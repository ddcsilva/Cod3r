// Maneira antiga
let exp = Math.pow(2, 3); // O primeiro parâmetro é a base e o segundo o expoente.
console.log(exp);

// ES7
let expNovo = 2 ** 3; // O primeiro parâmetro é a base e o segundo o expoente.
console.log(expNovo);

let base = 2;
base **= 3; // Eleva o valor armazenado na base à terceira potência
console.log(base);

let expNegativo = 2 ** -3; // Com expoente negativo
console.log(expNegativo);
